package com.alanaandnazar.qrscanner.model;

public class Announcement {
    String about;
    String date;

    public void setDate(String date) {
        this.date = date;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public String getDate() {
        return date;
    }

    public String getAbout() {
        return about;
    }
}
